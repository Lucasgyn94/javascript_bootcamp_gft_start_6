// '++' Incremento
console.log(`\t\tIncremento`);
let incremento = 0;
console.log(`Valor da variável antes do incremento: ${incremento}`);
incremento++;
console.log(`Valor da variável após do incremento: ${incremento}`);

// '--' Decremento
console.log(`\n\t\tDecremento`);
let decremento = 2;
console.log(`Valor da variável antes do decremento: ${decremento}`);
decremento--;
console.log(`Valor da variável após do decremento: ${decremento}`);
